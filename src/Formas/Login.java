    void validarIngreso(String sUsuario, String sPass){
    String ssql;   
        try {
            Connection cn= cc.conexion();            
            ssql = "";
            ssql = ssql + "select * from tusuario ";
            ssql = ssql + " where cod_usuario = " + "'" +sUsuario.toUpperCase()+"'";
            ssql = ssql + " and pass_usuario =" + "'" +sPass+ "'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(ssql);
             if(rs.next()){                 
                ssql = "";
                ssql = ssql + "select * from tusuario ";
                ssql = ssql + " where cod_usuario = " + "'" +sUsuario.toUpperCase()+"'";
                ssql = ssql + " and pass_usuario =" + "'" +sPass+ "'";
                ssql = ssql + " and sn_activo = 1";
                Statement stt = cn.createStatement();
                ResultSet rss = stt.executeQuery(ssql);
                if (rss.next()){                                                                                                 
                    user_logged ulo = new user_logged();
                    ulo.setUsy(rs.getString("txt_nombre_usu"));
                    ulo.setidUsu(rs.getInt("id_usuario"));    
                    user_logg.add(ulo);                                                                                
//                    Iterator<user_logged> itrLogg = user_logg.iterator();
//                    user_logged ul = itrLogg.next();
//                    JOptionPane.showMessageDialog(null, ul.getUsy() + " - " + ul.getidUsy() );                    
                    JOptionPane.showMessageDialog(null, "Bienvenido: "+ ulo.getUsy(),"CyCo",JOptionPane.INFORMATION_MESSAGE );
                    Menu_principal.user_memory = (ArrayList)user_logg.clone();
                    Menu_principal mp = new Menu_principal();                                                                                
                    mp.setVisible(true);                                                     
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(null, "El usuario ingresado esta dado de baja, no puede ingresar","CyCo",JOptionPane.INFORMATION_MESSAGE );
                }  
                rss.close();
                stt.close();
            }else{
               JOptionPane.showMessageDialog(null, "Usuario o contrase単a no coiciden ","INGRESRO",JOptionPane.INFORMATION_MESSAGE );
             }  
             rs.close();
             st.close();
             cn.close();
        } catch (Exception e ) {
//        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Error al validar:" + e,"Ingreso",JOptionPane.ERROR_MESSAGE );
        }              
    }
                                
