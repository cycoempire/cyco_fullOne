    private void jtbBitacoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbBitacoraMouseClicked
            
        try {
            if(evt.getClickCount()==2){ 
                
                if (this.jtbBitacora.getRowCount() == 0 ){ 
                    JOptionPane.showMessageDialog(null,"No hay productos eliminar","CyCo",JOptionPane.INFORMATION_MESSAGE); 
                    return;
                }
                
                if(jtbBitacora.getSelectedRow() >=0){
                    String iDf = String.valueOf(jtbBitacora.getValueAt(jtbBitacora.getSelectedRow(),0));                 
                    jfMostrarDescrip af = new jfMostrarDescrip();
                    af.jAreaDes.setText(iDf);
                    af.setVisible(true);                                                        
                }else{
                    JOptionPane.showMessageDialog(null, "Favor seleccione un registro para mostrar descripcion completa", "CyCo", JOptionPane.INFORMATION_MESSAGE);                            
                }    
                
                
                
                
                
                
                
                
            }
            
            
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jtbBitacoraMouseClicked

