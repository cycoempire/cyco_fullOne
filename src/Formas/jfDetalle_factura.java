    void mostrar_detalle(){
        String ssql;
        try {
            Connection cn = cc.conexion();
            DefaultTableModel bit = new DefaultTableModel();
            bit.addColumn("DESCIPCION");
            bit.addColumn("CANTIDAD");
            bit.addColumn("PRECIO");
            bit.addColumn("SUB-TOTAL");
            jtbBitacora.setModel(bit);
            
            //DESCRIPCION
            jtbBitacora.getColumnModel().getColumn(0).setMaxWidth(297);
            jtbBitacora.getColumnModel().getColumn(0).setMinWidth(297);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(297);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(0).setMinWidth(297);
        
            //CANTIDAD
            jtbBitacora.getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getColumnModel().getColumn(1).setMinWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMinWidth(99);
            
            //PRECIO
            jtbBitacora.getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getColumnModel().getColumn(1).setMinWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMinWidth(99);
            
            //SUBTOTAL
            jtbBitacora.getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getColumnModel().getColumn(1).setMinWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(99);
            jtbBitacora.getTableHeader().getColumnModel().getColumn(1).setMinWidth(99);
                       
        
            ssql = "";
            ssql = ssql + " select tp.txt_descripcion,td.cnt_producto,tpre.dPventa,td.sub_total_venta";
            ssql = ssql + " from tventa tf";
            ssql = ssql + " inner join tventa_detalle td on td.id_doc = tf.id_doc";
            ssql = ssql + " inner join tproducto tp on tp.id_producto = td.id_producto";
            ssql = ssql + " inner join tprecio tpre on tpre.id_producto = tp.id_producto";
            ssql = ssql + " where tf.id_doc =" +idFactura;
            ssql = ssql + " union all";
            ssql = ssql + " select ts.txt_desc_serv,ts.cantidad,ts.precio,ts.sub_total";
            ssql = ssql + " from tventa tf";
            ssql = ssql + " inner join tservicio ts on ts.id_fac = tf.id_doc";                                    
            ssql = ssql + " where tf.id_doc = " +idFactura;
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(ssql);
            String datos[] = new String[4];
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                bit.addRow(datos);            
            }
            jtbBitacora.setModel(bit);  
            rs.close();
            st.close();
            cn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar bitacora en: "+e, "CyCo", JOptionPane.INFORMATION_MESSAGE);
        }            
    }

